    private void salvarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarBtnActionPerformed
        EncomendaController controle = new EncomendaController();
        encomendaBuscada.setEnderecoEntrega(enderecoInput.getText());
        encomendaBuscada.setFrete(Float.valueOf(freteInput.getText()));
        encomendaBuscada.setValorTotal(Float.valueOf(valorInput.getText()));

        boolean inserido = controle.atualizarEncomenda(encomendaBuscada);
        if (inserido) {
            JOptionPane.showMessageDialog(null, "Categoria atualizada com sucesso!");
        } else {
            JOptionPane.showMessageDialog(null, "Falha ao atualizar a Categoria.");
        }

    }//GEN-LAST:event_salvarBtnActionPerformed

