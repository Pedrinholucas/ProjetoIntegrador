    private void okBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okBtnActionPerformed
        // TODO add your handling code here:
        TransportadoraController controle = new TransportadoraController();
        transportadoraBuscada = controle.buscarTransportadoraPorNome(input.getText()); //nome
        if (transportadoraBuscada == null) {
            JOptionPane.showMessageDialog(null, "Transportadora n√£o encontrada");
        } else{
            JOptionPane.showMessageDialog(null, "Transportadora encontrada com sucesso!");
            nome.setText(transportadoraBuscada.getNome());
            cnpjInput.setText(transportadoraBuscada.getCnpj());
        }

    }//GEN-LAST:event_okBtnActionPerformed

