    private void salvarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarBtnActionPerformed
        TransportadoraController controle = new TransportadoraController();
        transportadoraBuscada.setCnpj(cnpjInput.getText());
        transportadoraBuscada.setNome(nome.getText());
        boolean inserido = controle.atualizarTransportadora(transportadoraBuscada);
        if (inserido) {
            JOptionPane.showMessageDialog(null, "Transportadora atualizada com sucesso!");
        } else {
            JOptionPane.showMessageDialog(null, "Falha ao atualizar a Transportadora.");
        }

    }//GEN-LAST:event_salvarBtnActionPerformed

