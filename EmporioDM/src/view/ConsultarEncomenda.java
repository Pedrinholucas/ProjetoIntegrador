    private void okBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okBtnActionPerformed
        // TODO add your handling code here:
        EncomendaController controle = new EncomendaController();
        encomendaBuscada = controle.buscarEncomendaPorId(Integer.valueOf(input.getText())); //nome
        if (encomendaBuscada == null) {
            JOptionPane.showMessageDialog(null, "Categoria não encontrada");
        } else{
            JOptionPane.showMessageDialog(null, "Categoria encontrada com sucesso!");
            enderecoInput.setText(encomendaBuscada.getEnderecoEntrega());
            freteInput.setText(String.valueOf(encomendaBuscada.getFrete()));
            valorInput.setText(String.valueOf(encomendaBuscada.getValorTotal()));
        }

    }//GEN-LAST:event_okBtnActionPerformed

